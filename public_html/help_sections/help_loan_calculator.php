    <h4>üìù Understanding the Calculator Fields</h4>
    
    <p>The loan calculator requires four key pieces of information:</p>
    
    <ol class="help-steps">
        <li>
            <div class="step-title">Vehicle Price</div>
            <div class="step-description">
                Enter the total price of the car you're considering. This should be the listed price before taxes and fees.
                <ul style="margin: 1rem 0; padding-left: 2rem;">
                    <li><strong>Range:</strong> $1,000 - $200,000</li>
                    <li><strong>Tip:</strong> Use the exact price from the car listing for accuracy</li>
                </ul>
            </div>
        </li>
        
        <li>
            <div class="step-title">Down Payment</div>
            <div class="step-description">
                Enter how much you plan to pay upfront. A larger down payment reduces your monthly payment and total interest.
                <ul style="margin: 1rem 0; padding-left: 2rem;">
                    <li><strong>Typical Range:</strong> 10% - 25% of vehicle price</li>
                    <li><strong>Default:</strong> Calculator suggests 20% of the vehicle price</li>
                    <li><strong>Minimum:</strong> Many lenders require at least 10% down</li>
                </ul>
            </div>
        </li>
        
        <li>
            <div class="step-title">Loan Term</div>
            <div class="step-description">
                Choose how many years you want to finance the vehicle. Longer terms mean lower monthly payments but more total interest.
                <ul style="margin: 1rem 0; padding-left: 2rem;">
                    <li><strong>Short Term (2-3 years):</strong> Higher monthly payments, less total interest</li>
                    <li><strong>Medium Term (4-5 years):</strong> Balanced payments and interest</li>
                    <li><strong>Long Term (6-7 years):</strong> Lower monthly payments, more total interest</li>
                </ul>
            </div>
        </li>
        
        <li>
            <div class="step-title">Interest Rate</div>
            <div class="step-description">
                Enter the annual percentage rate (APR) for your loan. This depends on your credit score, loan term, and current market rates.
                <ul style="margin: 1rem 0; padding-left: 2rem;">
                    <li><strong>Excellent Credit (750+):</strong> 3% - 5%</li>
                    <li><strong>Good Credit (650-749):</strong> 5% - 8%</li>
                    <li><strong>Fair Credit (600-649):</strong> 8% - 12%</li>
                    <li><strong>Poor Credit (below 600):</strong> 12% - 20%</li>
                </ul>
            </div>
        </li>
    </ol>
    
    <img src="../images/help/calculator-form.png" alt="Loan calculator form showing all input fields with sample values" class="help-screenshot">
    
    <div class="help-tip">
        <strong>Don't Know Your Interest Rate?</strong> Start with an estimate based on your credit score, then contact our financing team for a personalized quote.
    </div>
    
    <h4>üìä Understanding Your Results</h4>
    
    <p>Once you click "Calculate Payment," you'll see comprehensive results:</p>
    
    <ol class="help-steps">
        <li>
            <div class="step-title">Monthly Payment</div>
            <div class="step-description">
                This is your estimated monthly payment amount. Make sure this fits comfortably in your budget, leaving room for insurance, maintenance, and other expenses.
            </div>
        </li>
        
        <li>
            <div class="step-title">Total Payment</div>
            <div class="step-description">
                The total amount you'll pay over the life of the loan, including the down payment. This helps you understand the true cost of the vehicle.
            </div>
        </li>
        
        <li>
            <div class="step-title">Total Interest</div>
            <div class="step-description">
                The total interest you'll pay over the loan term. Compare different scenarios to minimize this amount.
            </div>
        </li>
        
        <li>
            <div class="step-title">Payment Breakdown</div>
            <div class="step-description">
                Detailed breakdown showing:
                <ul style="margin: 1rem 0; padding-left: 2rem;">
                    <li>Principal amount (loan amount after down payment)</li>
                    <li>Down payment amount</li>
                    <li>Interest rate and loan term summary</li>
                </ul>
            </div>
        </li>
    </ol>
    
