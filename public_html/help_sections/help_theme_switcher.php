    <h4>🚫 Troubleshooting Theme Issues</h4>
    
    <p>If you experience problems with themes:</p>
    
    <ol class="help-steps">
        <li>
            <div class="step-title">Theme Not Changing</div>
            <div class="step-description">
                If the theme doesn't change when you select it:
                <ul style="margin: 1rem 0; padding-left: 2rem;">
                    <li>Refresh the page and try again</li>
                    <li>Clear your browser cache and cookies</li>
                    <li>Ensure JavaScript is enabled in your browser</li>
                </ul>
            </div>
        </li>
        
        <li>
            <div class="step-title">Theme Not Saving</div>
            <div class="step-description">
                If your theme choice isn't remembered between visits:
                <ul style="margin: 1rem 0; padding-left: 2rem;">
                    <li>Check that your browser allows local storage</li>
                    <li>Make sure you're not browsing in incognito/private mode</li>
                    <li>Try disabling browser extensions that might interfere</li>
                </ul>
            </div>
        </li>
        
        <li>
            <div class="step-title">Display Issues</div>
            <div class="step-description">
                If colors or layout appear incorrect:
                <ul style="margin: 1rem 0; padding-left: 2rem;">
                    <li>Try switching to a different theme and back</li>
                    <li>Update your browser to the latest version</li>
                    <li>Contact support if problems persist</li>
                </ul>
            </div>
        </li>
    </ol>
    
    <div class="help-warning">
        <strong>Browser Compatibility:</strong> Very old browsers (Internet Explorer, browsers over 5 years old) may not support theme switching. We recommend updating to a modern browser for the best experience.
